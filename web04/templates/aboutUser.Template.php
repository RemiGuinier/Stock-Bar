            <h2 class="featurette-heading">A propos du jeu
            </h2>
            <p class="lead">
			Le principe consiste à prendre possession des lignes de chemins de fer entre des villes d'un continent, sous-continent ou pays, avec des wagons.<br /><br />

					Un joueur peut marquer des points de trois manières :

						<li>en plaçant ses wagons sur des tronçons reliant des villes (plus le tronçon est long, plus le nombre de points est élevé) ;</li>
						<li>en réalisant les contrats qu'il possède ;</li>
						<li>en réalisant le chemin le plus long passant par plusieurs villes.</li><br /><br/>

					Pendant son tour de jeu, un joueur peut :<br />

						<li>soit piocher deux cartes « wagons » (ou une seule s'il prend un joker visible) ;</li>
						<li>soit prendre possession d'une ligne entre deux villes en défaussant les cartes « wagons » correspondantes ;</li>
						<li>soit piocher de nouveaux contrats.</li><br />
						Les Aventuriers du Rail peut se jouer de 2 à 6 joueurs, en fonction des plateaux de jeu. Une partie dure en moyenne entre 30 et 60 minutes.<br /><br />
						
